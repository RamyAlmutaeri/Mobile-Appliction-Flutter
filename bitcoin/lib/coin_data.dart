const List<String> cryptoList = [
  'BTC',
  'ETH',
  'LTC',
];
const coinAPIURL = 'https://rest.coinapi.io/v1/exchangerate';
const apiKey = 'F42CB09A-95A4-466D-8269-A91D0BC3F967';

class CoinData {
  Future getCoinData(String currency) async {
    //TODO 4: Use a for loop here to loop through the cryptoList and request the data for each of them in turn.
    //TODO 5: Return a Map of the results instead of a single value.
    Map<String, String> cryptoPrices = {};
    for (String crypto in cryptoList) {
      http.Response response = await http.get(
        Uri.parse('$coinAPIURL/$crypto/$currency?apikey=$apiKey'),
      );
      if (response.statusCode == 200) {
        var data = jsonDecode(response.body);
        double price = await data['rate'];
        cryptoPrices[crypto] = price.toStringAsFixed(0);
      } else {
        print(response.statusCode);
        throw 'Problem with the get request';
      }
    }
    return cryptoPrices;
  }
}
